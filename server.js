/**
 * Ella Rises INTEX Project - Main Server File
 * Node/Express application for managing Ella Rises data
 */

const express = require('express');
const session = require('express-session');
const path = require('path');
require('dotenv').config();

const app = express();
const PORT = process.env.PORT || 3000;

// Middleware
app.use(express.json());
app.use(express.urlencoded({ extended: true }));
app.use(express.static(path.join(__dirname, 'public')));

// Session configuration
app.use(session({
  secret: process.env.SESSION_SECRET || 'ella-rises-secret-key-change-in-production',
  resave: false,
  saveUninitialized: false,
  cookie: {
    secure: false, // Set to true in production with HTTPS
    httpOnly: true,
    maxAge: 24 * 60 * 60 * 1000 // 24 hours
  }
}));

// View engine setup
app.set('view engine', 'ejs');
app.set('views', path.join(__dirname, 'views'));

// All routes consolidated in routes/index.js
const routes = require('./routes');

// Use routes
app.use('/', routes);

// Start server
app.listen(PORT, () => {
  console.log(`Ella Rises server running on http://localhost:${PORT}`);
});

